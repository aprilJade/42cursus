int	ft_strcmp(char *s1, char *s2)
{
	int	ret;

	ret = 0;
	while (ret == 0)
	{
		ret = *s1 - *s2;
		if (*s1 == '\0' || *s2 == '\0')
			break ;
		s1++;
		s2++;
	}
	if (ret > 0)
		ret = 1;
	else if (ret < 0)
		ret = -1;
	else
		ret = 0;
	return (ret);
}
