#include <stddef.h>

void	*ft_memchr(const void *s, int c, size_t n)
{
	unsigned char	*ps;
	size_t			i;

	i = 0;
	ps = (unsigned char *)s;
	while (i++ < n)
		if (*ps++ == (unsigned char)c)
			return (ps - 1);
	return (0);
}
